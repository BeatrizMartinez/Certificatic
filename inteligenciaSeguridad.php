<p id="texto" style="text-align: justify; width: 70%; color: #585858; font-size: 16px;">
La inteligencia en su más pura definición es la capacidad de elegir, entre varias posibilidades, la opción más acertada a la resolución de un problema.
</p>
<p id="texto" style="text-align: justify; width: 70%; color: #585858; font-size: 16px;">
Los tradicionales Dashboards obtenidos después de un semestre de hacer ETL, higienización de datos, un Data Warehouse o Data Mart, solo dan indicadores primarios, pero, en un mundo tan ágil, ¿Por qué tomar información de lo que se tiene en el pasado en fuentes estructuradas únicamente? Existen cantidad de reportes, documentos y demás información no estructurada (documentos tipo Word®, PDF®, Excel®, entre otros) donde la información es más actualizada y permite tomar datos para ver las situaciones en 360° para mejorar la toma de decisiones y procurar bajar el riesgo.
</p>

<p id="texto" style="text-align: justify; width: 70%; color: #585858; font-size: 16px;">
