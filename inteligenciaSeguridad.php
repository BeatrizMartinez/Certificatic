<h3 style="border-left: #FE2E2E 5px solid;  text-align: left;  padding-left: 10px;"  id="texto">Acceso Unificado a la Información</h3>
<p style="text-align: left; padding-left: 0px; font-size: 13px; "  id="texto">
<b>MakingPIT une esfuerzos con DSA Soluciones para ofrecer soluciones de Acceso Unificado a la Información.</b>
</p>
</div>

<p id="texto" style="text-align: justify; width: 70%; color: #585858; font-size: 16px;">
Cuando la información necesaria para la toma de decisión de un usuario de negocio o una aplicación está distribuida en más de un sistema, en más de un formato, las decisiones o procesos frecuentemente se retrasan, ocasionando la pérdida de oportunidades en vez de dar resultados.
</p>

<p id="texto" style="text-align: justify; width: 70%; color: #585858; font-size: 16px;">
Cuando el costo de oportunidad depende de sistemas heredados que al recuperar información compleja retrasan la detección, aumenta el riesgo, incrementa la posible penalización de regulaciones, acrecenta la frustración no solo del usuario, sino también de pérdidas al negocio.
</p>

<p id="texto" style="text-align: justify; width: 70%; color: #585858; font-size: 16px;">
El Acceso Unificado a la Información combina información diversa sin depender en capas de datos y sin sacrificar la relación y servicios analíticos que al vuelo pueden ejecutarse, dándole sentido y significado a la información, hallando la relación no evidente.
</p>

<p id="texto" style="text-align: justify; width: 70%; color: #585858; font-size: 16px;">
<b>MakingPIT</b> le permite obtener en menor tiempo y con menor gasto, al negocio un compromiso para obtener información de sus fuentes y múltiples de datos.
</p>

<p id="texto" style="text-align: justify; width: 70%; color: #585858; font-size: 16px;">
Nuestro modelo de madurez de acceso a la información le permitirá en etapas o en conjunto obtener:
</p>
